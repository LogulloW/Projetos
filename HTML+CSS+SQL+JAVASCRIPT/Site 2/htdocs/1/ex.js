/*******************************************************/
function clicar() {
    var a = window.document.getElementById('area')
    a.style.background = 'rgba(0, 0, 0, 100)'
}

function clicarl() {
    var a = window.document.getElementById('area')
    a.style.background = 'rgba(0, 0, 0, 0.6)'
}

function entrar() {
    var a = window.document.getElementById('area')
    var img = window.document.getElementById('imagi')
    a.style.background = 'rgba(0, 0, 0, 0.7)'
    a.style.color = 'white'
    img.src = 'darling.gif' 
}

function sair() {
    var a = window.document.getElementById('area')
    var img = window.document.getElementById('imagi')
    a.style.background = 'rgba(0, 0, 0, 0.6)'
    a.style.color = 'rgba(255, 255, 255, 0.85)'
    img.src = 'darlingyt.jpg'
}
